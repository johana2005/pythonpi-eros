i=1 # El signo i es para referenciar que vale uno, se declara la variable i

sum=0 # Se declara la variable suma, iguala cero para que inicie sumando un cero  
while i<=5: # Se realiza para declarar que el bucle es verdadero 
    print(i) #imprimir en pantalla la variable i 

    sum=sum+i #realiza la operacion suma mas la i 

    i+=1 #i=i+1 
print(f'la suma es ={sum}') # imprimir en pantalla "la suma es" mas la variable suma  
